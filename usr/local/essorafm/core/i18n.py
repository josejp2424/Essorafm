def _lang_code():
    raw = os.environ.get('LANG', '') or locale.getdefaultlocale()[0] or 'en'
    raw = raw.split('.')[0]
    raw = raw.replace('-', '_')
    if raw.startswith('zh'):
        return 'zh'
    if raw.startswith('ja') or raw.startswith('jp'):
        return 'ja'
    return raw.split('_')[0] if '_' in raw else raw

